package org.college;

public class College {
	
	public String collegeName(String str)
	{
		System.out.println("The name of the college: "+str);
		return str;
	}
	
	public int code(int num)
	{
		System.out.println("The Code of the college: "+num);
		return num;
	}

	public int rank(int num)
	{
		System.out.println("The Rank of the college: "+num);
		return num;
	}


}
