package week1.day2;

public class Calculator {
	
	public int performadd ( int a, int b, int c)
	{
		int d = a+b+c;
		return d;
	}
	public int performsub ( int a, int b)
	{
		int c = a-b;
		return c;
	}
	public double performmul ( double a, double b)
	{
		double c = a*b;
		return c;
	}
	public float performdiv ( float a, float b)
	{
		float c = a/b;
		return c;
	}

}
